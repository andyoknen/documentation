<h2 id="content-creators" tabindex="-1"><a class="header-anchor" href="#content-creators"><span>Content Creators</span></a></h2>
<p>Bastyon believes that content creators should be well compensated for their work. A new advertising and monetization program for bloggers has been launched on the platform.</p>
<h3 id="how-it-works" tabindex="-1"><a class="header-anchor" href="#how-it-works"><span>How It Works</span></a></h3>
<ul>
<li>Advertisers promote products or services on Bastyon</li>
<li>Ads appear in the main feed and author channels</li>
<li>Earnings from the main feed go to nodes</li>
<li>Earnings from author channels go directly to the authors</li>
</ul>
<h3 id="participants-and-payouts" tabindex="-1"><a class="header-anchor" href="#participants-and-payouts"><span>Participants and Payouts</span></a></h3>
<ul>
<li>Initially, monetization works only on verified author channels</li>
<li>Authors can disable monetization</li>
<li>Channels with high activity will be included over time</li>
<li>Channels outside Bastyon with over 1,000 subscribers can request verification via <a href="mailto:support@bastyon.com" target="_blank" rel="noopener noreferrer">email</a></li>
</ul>
<blockquote>
<p><strong>IMPORTANT:</strong> Authors earn proportionally to the activity and number of people on their channel. Higher engagement leads to higher payouts from the advertising pool.</p>
</blockquote>
